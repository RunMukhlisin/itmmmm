    private void tItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tItemMouseClicked
        // TODO add your handling code here:
        try {
            int selectedRow = tItem.rowAtPoint(evt.getPoint());

            // Mengambil data dari tabel dengan pemeriksaan null
            String idItem = tItem.getValueAt(selectedRow, 0).toString();
            String kategori = tItem.getValueAt(selectedRow, 1).toString();
            String lemari = tItem.getValueAt(selectedRow, 2).toString();
            String namaBarang = tItem.getValueAt(selectedRow, 3).toString();
            String penulis = tItem.getValueAt(selectedRow, 4).toString();
            String tahunTerbit = tItem.getValueAt(selectedRow, 5).toString();
            String jumlah = tItem.getValueAt(selectedRow, 6).toString();

            // Membuka PopUp_Item1
            PopUp_Item1 popup = new PopUp_Item1();
            popup.Lb_item.setText(idItem);
            popup.cKategori.setSelectedItem(kategori);
            popup.tLemari.setText(lemari);
            popup.tNamaBarang.setText(namaBarang);
            popup.tPenulis.setText(penulis);
            popup.tTahunTerbit.setText(tahunTerbit);
            popup.tJumlah.setText(jumlah);
            popup.setVisible(true);

        } catch (SQLException e) {
            Logger.getLogger(menuItem.class.getName()).log(Level.SEVERE, "Error handling table click", e);
        }
    }//GEN-LAST:event_tItemMouseClicked


