     private void loadTable() {
        // Inisialisasi model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Item");
        model.addColumn("Kategori");
        model.addColumn("Lemari");
        model.addColumn("Judul");
        model.addColumn("Penulis");
        model.addColumn("Tahun Terbit");
        model.addColumn("Jumlah");
        model.addColumn("File");

        try {
            Item itemHandler = new Item();
            ResultSet resultSet = itemHandler.tampilItem();

            // Iterasi hasil query dan tambahkan ke model tabel
            while (resultSet.next()) {
                model.addRow(new Object[] {
                    resultSet.getInt("id_item"),
                    resultSet.getString("nama_kategori"),
                    resultSet.getString("lemari"),
                    resultSet.getString("nama_barang"),
                    resultSet.getString("penulis"),
                    resultSet.getInt("tahun_terbit"),
                    resultSet.getInt("jumlah"),
                    resultSet.getString("file")
                });
            }
        } catch (SQLException e) {
            Logger.getLogger(menuItem.class.getName()).log(Level.SEVERE, "Error loading table data", e);
        }

        // Set model ke tabel
        tItem.setModel(model);
    }
    

