    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        // TODO add your handling code here:
        try {
        // Validasi ID
        if (lb_Id.getText().isEmpty() || !lb_Id.getText().matches("\\d+")) {
            throw new IllegalArgumentException("ID Anggota tidak valid. Pastikan ID berupa angka dan tidak kosong.");
        }

        // Konfirmasi sebelum menghapus
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", 
                                                    "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            // Hapus data
            Anggota cty = new Anggota();
            cty.setIdAnggota(Integer.parseInt(lb_Id.getText()));
            cty.hapusAnggota();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!", "Berhasil", JOptionPane.INFORMATION_MESSAGE);

            // Reset form setelah penghapusan
            reset();
            
            pnUtama.removeAll();
        pnUtama.add(new menuAnggota());
        pnUtama.repaint();
        pnUtama.revalidate();
        }
    } catch (IllegalArgumentException e) {
        // Tampilkan pesan error validasi
        JOptionPane.showMessageDialog(null, e.getMessage(), "Validasi Gagal", JOptionPane.ERROR_MESSAGE);
    } catch (SQLException sQLException) {
        // Tampilkan pesan error SQL
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menghapus data. Periksa koneksi atau data Anda!", 
                                      "Kesalahan Database", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_bHapusActionPerformed

