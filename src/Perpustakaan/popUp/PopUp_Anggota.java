    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        try {
        Anggota ang = new Anggota();

        // Validasi ID Anggota
        if (lb_Id.getText().isEmpty() || !lb_Id.getText().matches("\\d+")) {
        throw new IllegalArgumentException("NIM harus berupa angka dan tidak boleh kosong.");
        }
        ang.setIdAnggota(Integer.parseInt(lb_Id.getText()));

        // Validasi Nama
        if (tNama.getText().isEmpty() || !tNama.getText().matches("[a-zA-Z\\s]+") || tNama.getText().length() < 3) {
        throw new IllegalArgumentException("Nama hanya boleh berisi huruf, tidak boleh kosong, dan harus memiliki minimal 3 karakter.");
        }
        ang.setNama(tNama.getText());

        // Validasi Alamat
        if (tAlamat.getText().isEmpty()) {
        throw new IllegalArgumentException("Alamat tidak boleh kosong.");
        }
        ang.setAlamat(tAlamat.getText());

        // Validasi Nomor HP
        if (tNoHp.getText().isEmpty() || !tNoHp.getText().matches("\\d{10,13}")) {
        throw new IllegalArgumentException("Nomor HP harus berupa angka, tidak boleh mengandung huruf, dan memiliki 10-13 digit.");
        }
        ang.setNoHp(tNoHp.getText());

        // Validasi Email
        if (tEmail.getText().isEmpty() || !tEmail.getText().matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
        throw new IllegalArgumentException("Email tidak valid atau kosong.");
        }
        ang.setEmail(tEmail.getText());

        // Validasi NIM/NIDN
        if (tNim.getText().isEmpty() || !tNim.getText().matches("\\d{5,16}")) {
        throw new IllegalArgumentException("NIM/NIDN harus berupa angka, tidak boleh huruf, memiliki 5-16 digit, dan tidak boleh kosong.");
        }
        ang.setNim(tNim.getText());

        // Tambahkan data anggota ke database
        ang.tambahAnggota();
        System.out.println("Data berhasil disimpan!");

        // Reset form
        reset();
    
         // Menyimpan data kategori ke database;
        reset(); // Mereset form setelah penyimpanan
        autoId(); // Mengatur ulang ID kategori otomatis
        
        pnUtama.removeAll();
        pnUtama.add(new menuAnggota());
        pnUtama.repaint();
        pnUtama.revalidate();
    
        } catch (IllegalArgumentException e) {
        // Tampilkan pesan error validasi
        javax.swing.JOptionPane.showMessageDialog(null, e.getMessage(), "Validasi Gagal", javax.swing.JOptionPane.ERROR_MESSAGE);
    }    catch (SQLException sQLException) {
    // Tangani error SQL
    javax.swing.JOptionPane.showMessageDialog(null, "Data tidak masuk ke database. Periksa koneksi atau data!", "Kesalahan Database", javax.swing.JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_bSimpanActionPerformed

