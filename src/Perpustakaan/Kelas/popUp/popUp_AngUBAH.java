    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // TODO add your handling code here:
        try {
        // Membuat instance dari kelas Anggota
        Anggota pu = new Anggota();
        
        // Mengatur nilai properti pada objek Anggota
        pu.setIdAnggota(Integer.parseInt(lb_Id.getText())); // Mengambil ID dari label lb_Id
        pu.setNama(tNama.getText());
        pu.setAlamat(tAlamat.getText());
        pu.setNoHp(tNoHp.getText());
        pu.setEmail(tEmail.getText());
        pu.setNim(tNim.getText());

        // Memanggil metode ubahAnggota untuk memperbarui data
        pu.ubahAnggota();

        // Memberikan pesan sukses
        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
        
        // Menutup form setelah berhasil
        dispose();
        
        pu.tambahAnggota(); // Menyimpan data kategori ke database;
        
        pnUtama.removeAll();
        pnUtama.add(new menuAnggota());
        pnUtama.repaint();
        pnUtama.revalidate();
    } catch (SQLException ex) {
        // Menampilkan pesan kesalahan jika ada masalah
        javax.swing.JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + ex.getMessage());
        ex.printStackTrace();
    } catch (NumberFormatException ex) {
        // Menangani kesalahan format jika lb_Id bukan angka
        javax.swing.JOptionPane.showMessageDialog(this, "ID Anggota harus berupa angka.");
    }
    }//GEN-LAST:event_bUbahActionPerformed

