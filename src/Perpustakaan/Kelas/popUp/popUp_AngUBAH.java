    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        // TODO add your handling code here:
        try {
        // Memastikan ID anggota diambil dari komponen yang benar
        int idAnggota = Integer.parseInt(lb_Id.getText());
        
        // Menampilkan konfirmasi sebelum menghapus
        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Apakah Anda yakin ingin menghapus data anggota ini?", 
            "Konfirmasi Hapus", 
            javax.swing.JOptionPane.YES_NO_OPTION);

        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            // Membuat instance dari kelas Anggota
            Anggota cty = new Anggota();
            cty.setIdAnggota(idAnggota);

            // Memanggil metode hapusAnggota untuk menghapus data
            cty.hapusAnggota();

            // Menampilkan pesan sukses
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");

            // Menutup form setelah berhasil
            dispose();
            
        pnUtama.removeAll();
        pnUtama.add(new menuAnggota());
        pnUtama.repaint();
        pnUtama.revalidate();
        }
    } catch (NumberFormatException ex) {
        // Menangani kesalahan format jika lb_umum bukan angka
        javax.swing.JOptionPane.showMessageDialog(this, "ID Anggota harus berupa angka.");
    } catch (SQLException ex) {
        // Menangani kesalahan jika terjadi masalah saat menghapus data
        javax.swing.JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_bHapusActionPerformed

