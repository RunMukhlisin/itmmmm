    private void bLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLoginActionPerformed
        if (tUsername.getText().isBlank() && tPassword.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Username/Password kosong");
        } else {
            try {
                user user = new user();
                user.setUsername(tUsername.getText());
                user.setPassword(tPassword.getText());

                user.login();
                if (session.getSesi().equals("Aktif")) {
                    new menu().setVisible(true);
                    dispose();
                }
            } catch (SQLException sQLException) {
            }
        }

    }//GEN-LAST:event_bLoginActionPerformed

