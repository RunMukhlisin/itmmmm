    private void bRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRegisterActionPerformed
        try {
            if (tNamaAwal.getText().isEmpty()
                    || tUsername.getText().isEmpty()
                    || tEmail.getText().isEmpty() || !tEmail.getText().contains("@gmail.com")
                    || (!bSuperAdmin.isSelected() && !bAdmin.isSelected())
                    || (!kepegawaian.isSelected() && !bukuInduk.isSelected() && !pembayaran.isSelected()
                    && !inventaris.isSelected() && !manajemenSurat.isSelected() && !Perpustakaan.isSelected())
                    || tPassword.getText().isBlank()) {
                
                JOptionPane.showMessageDialog(null, "Data belum diisi dengan benar");
                return; // Hentikan proses jika validasi gagal
            }

            // Jika validasi lolos, cetak data
            user reg = new user();
            reg.setNama_depan(tNamaAwal.getText());
            reg.setNama_belakang(tNamaAkhir.getText());
            reg.setUsername(tUsername.getText());
            reg.setEmail(tEmail.getText());
            
            if (bSuperAdmin.isSelected()) {
                reg.setPrivillage(2);
            } else {
                reg.setPrivillage(1);
            }
            
            if (kepegawaian.isSelected()) {
                reg.setPriv_kepegawaian(1);
            }else{
                reg.setPriv_kepegawaian(0);
            }
            if (bukuInduk.isSelected()) {
                reg.setPriv_bukuInduk(1);
            }else{
                reg.setPriv_bukuInduk(0);
            }
            if (pembayaran.isSelected()) {
                reg.setPriv_pembayaran(1);
            }else{
                reg.setPriv_pembayaran(0);
            }
            if (inventaris.isSelected()) {
                reg.setPriv_inventaris(1);
            }else{
                reg.setPriv_inventaris(0);
            }
            if (manajemenSurat.isSelected()) {
                reg.setPriv_surat(1);
            }else{
                reg.setPriv_surat(1);
            }
            if (Perpustakaan.isSelected()) {
                reg.setPriv_perpustakaan(1);
            }else{
                reg.setPriv_perpustakaan(0);
            }
            reg.setPassword(tPassword.getText());
            reg.setId_user(tID.getText());
            reg.tambahUser();
            dispose();
            new Login().setVisible(true);
            
        } catch (HeadlessException headlessException) {
        } catch (SQLException sQLException) {
        }        
    }//GEN-LAST:event_bRegisterActionPerformed

