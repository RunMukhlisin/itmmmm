    private void tbDetailMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDetailMouseClicked
        int row = tbDetail.getSelectedRow();
        String status = tbDetail.getModel().getValueAt(row, 4).toString();

        if (status.equals("CICILAN")) {
            bBayarC.setVisible(true);
        }
    }//GEN-LAST:event_tbDetailMouseClicked

    private void bBayarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBayarCActionPerformed
        int row = tbDetail.getSelectedRow();
        int hargaAsli = Integer.parseInt(tbDetail.getModel().getValueAt(row, 4).toString().replace(".", ""));
        int hargaCicil = Integer.parseInt(tbDetail.getModel().getValueAt(row, 3).toString().replace(".", ""));
        int hargaKurang = hargaAsli - hargaCicil;
        Pembayaran.setNominal_pembayaran(hargaKurang);
        Pembayaran.setNominal_jenis(hargaAsli);
        Pembayaran.setNomial_dibayar(hargaCicil);
        jenis_pembayaran.setNama_jenis(tbDetail.getModel().getValueAt(row, 2).toString());
        pnKonten.removeAll();
        pnKonten.add(new pnCicil());
        pnKonten.repaint();
        pnKonten.revalidate();
    }//GEN-LAST:event_bBayarCActionPerformed


