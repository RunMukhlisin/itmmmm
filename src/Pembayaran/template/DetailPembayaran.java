void getData2() {
    tNIM.setText(Pembayaran.getNim());
    tJudul.setText("Pembayaran " + mahasiswa.getNama());

    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Tanggal");
    model.addColumn("Pembayaran");
    model.addColumn("Nominal Dibayar");
    model.addColumn("Nominal");
    model.addColumn("Status");
    model.addColumn("Keterangan");

    try {
        Pembayaran pem = new Pembayaran();
        ResultSet data = pem.tampilUt();

        while (data.next()) {
            String bayar = String.valueOf(data.getInt("Nominal Dibayar"));
            String nominal = String.valueOf(data.getInt("Nominal"));
            double nombar = Double.parseDouble(bayar);
            double nom = Double.parseDouble(nominal);
            DecimalFormat df = new DecimalFormat("#,###,###");
            String nominalBayar = df.format(nombar);
            String nominall = df.format(nom);
            model.addRow(new Object[]{
                data.getString("ID Pembayaran"),
                data.getString("Tanggal"),
                data.getString("Nama Jenis"),
                nominalBayar,
                nominall,
                data.getString("Status"),
                data.getString("Keterangan")
            });
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    tbDetail.setModel(model);

    // Sembunyikan kolom "ID" (indeks 0) dan "Nominal" (indeks 4)
    tbDetail.getColumnModel().removeColumn(tbDetail.getColumnModel().getColumn(0)); // Kolom ID
    tbDetail.getColumnModel().removeColumn(tbDetail.getColumnModel().getColumn(3)); // Kolom Nominal
}

