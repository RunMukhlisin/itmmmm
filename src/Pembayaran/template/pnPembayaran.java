    private void bBayCetkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBayCetkActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) tbPembayaran.getModel();

            // Ambil jumlah baris dalam tabel
            int rowCount = model.getRowCount();

            // Perulangan untuk membaca setiap baris
            for (int i = 0; i < rowCount; i++) {
                // Ambil data dari setiap kolom di baris ke-i
                String nim = model.getValueAt(i, 0).toString();    // Kolom 0
                String nama = model.getValueAt(i, 1).toString();   // Kolom 1
                String angkatan = model.getValueAt(i, 2).toString();     // Kolom 2
                String prodi = model.getValueAt(i, 3).toString();  // Kolom 3

                mahasiswa.setNim(nim);
                mahasiswa.setNama(nama);
                mahasiswa.setAngkatan(Integer.parseInt(angkatan));
                mahasiswa.setProdi(prodi);

                jenis_pembayaran jp = new jenis_pembayaran();
                jenis_pembayaran.setNama_jenis(cJenis.getSelectedItem().toString());
                ResultSet Data = jp.Konversi2();
                Pembayaran pem = new Pembayaran();
                Pembayaran.setNim(nim);
                if (Data.next()) {
                    String bayar = tNomBay.getText().replace(".", "");
                    Pembayaran.setId_pembayaran(tID.getText());
                    Pembayaran.setId_jenis(Data.getString("id_jenis"));
                    Pembayaran.setNominal_pembayaran(Integer.parseInt(bayar));
                    if (!tKeterangan.equals(null)) {
                        Pembayaran.setKeterangan(tKeterangan.getText());
                    } else {
                        Pembayaran.setKeterangan(null);
                    }

                    if (cBayar.getSelectedItem().equals("LUNAS")) {
                        Pembayaran.setStatus(1);
                    } else if (cBayar.getSelectedItem().equals("CICIL")) {
                        Pembayaran.setStatus(2);
                    }
                    pem.tambahPemUt();
                    Print();

                    pb_sementara pbn = new pb_sementara();
                    pbn.hapus();
                    getData();
                    autoID();

                }
            }

        } catch (SQLException sQLException) {
        }
        JOptionPane.showMessageDialog(null, "PEMBAYARAN SELESAI");
        pnKonten.removeAll();
        pnKonten.add(new History());
        pnKonten.repaint();
        pnKonten.revalidate();
    }//GEN-LAST:event_bBayCetkActionPerformed

