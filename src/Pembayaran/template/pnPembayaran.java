    private void cBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cBayarActionPerformed

        if (cBayar.getSelectedItem().equals("LUNAS")) {
            tNomBay.setText(lblHarga.getText().substring(3));
            bBayar.setVisible(true);
            bBayCetk.setVisible(true);
        } else if (cBayar.getSelectedItem().equals("CICIL")) {
            if (cBayar.getSelectedItem().equals("- PILIH -")) {
                JOptionPane.showMessageDialog(null, "NOMINAL BAYAR BELUM DIISI");
                cBayar.setSelectedIndex(0);
                bBayar.setVisible(false);
                bBayCetk.setVisible(false);
            } else {
                int bayar = Integer.parseInt(tNomBay.getText().replace(".", ""));
                int harga = Integer.parseInt(lblHarga.getText().replace(".", "").substring(2));
                bBayar.setVisible(true);
                bBayCetk.setVisible(true);
                if (bayar == harga) {
                    JOptionPane.showMessageDialog(null, "NOMINAL BAYAR LUNAS");
                    cBayar.setSelectedIndex(0);
                    bBayar.setVisible(false);
                    bBayCetk.setVisible(false);
                }
            }

        }

    }//GEN-LAST:event_cBayarActionPerformed

    private void tNomBayKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tNomBayKeyReleased
        String nominal = tNomBay.getText().replaceAll("[^\\d]", "");
        double Rp = Double.parseDouble(nominal);
        DecimalFormat df = new DecimalFormat("#,###,###");
        tNomBay.setText(df.format(Rp));

        int bayar = Integer.parseInt(tNomBay.getText().replace(".", ""));
        int harga = Integer.parseInt(lblHarga.getText().replace(".", "").substring(2));
        if (bayar > harga) {
            peringatan.setVisible(true);
            cBayar.setSelectedIndex(0);
            cBayar.setEnabled(false);
        } else {
            peringatan.setVisible(false);
            cBayar.setEnabled(true);
        }
    }//GEN-LAST:event_tNomBayKeyReleased

